<?php 
//awal penjumlahan//
	function penjumlahan($bil_A, $bil_B){
		$hasil=0;
		$hasil = $bil_A + $bil_B;
		return $hasil;
	}
	
//akhir penjumlahan//

//awal pengurangan//
	function pengurangan ($bil_A, $bil_B){
		$hasil=0;
		$hasil = ($bil_A - $bil_B);
		return $hasil;
	}
	
//akhir pengurangan//

//awal perkalian
	function perkalian ($bil_A, $bil_B){
		$hasil=0;
		$hasil = ($bil_A * $bil_B);
		return $hasil;
	}

	function pembagian ($bil_A, $bil_B){
		$hasil=0;
		$hasil = ($bil_A / $bil_B);
		return $hasil;
	}

 ?>